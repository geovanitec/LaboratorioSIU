    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        //Codigo que permite consultar registros en la base de datos
        try{
            //local host y el nombre de la base de datos y la contrase√±√±a
            Connection cn = DriverManager.getConnection(server, user, password);
            //Esto permitir√° el buscar registro a traves del ID
            PreparedStatement pst = cn.prepareStatement("select * from cursos where codigoCursos = ?");
            //El parametro 1 es de que solo un ? esta llenando y entra lo del txt
            pst.setString(1, txt_buscar.getText().trim());

            //El result del query, lo que ira a traer
            ResultSet rs = pst.executeQuery();

            if(rs.next()){
                txtCodigoCurso.setText(rs.getString("codigoCursos"));
                txtNombreCurso.setText(rs.getString("nombreCursos"));
                txtEstatus.setText(rs.getString("estatusCursos"));

                JOptionPane.showMessageDialog(null, "Curso encontrado.");
            } else {
                JOptionPane.showMessageDialog(null, "Curso no encontrado.");
            }

        }catch (Exception e){

        }
    }//GEN-LAST:event_btnBuscarActionPerformed

    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        //Codigo que permite insertar registros en al base de datos
        try{
            //local host y el nombre de la base de datos y la contrase√±a
            Connection cn = DriverManager.getConnection(server, user, password);
            //Esto permitir√° el insert
            PreparedStatement pst = cn.prepareStatement("insert into cursos values(?,?,?)");

            //El primer parametro de cada setString es cada ?
            pst.setString(1, txtCodigoCurso.getText().trim());
            pst.setString(2, txtNombreCurso.getText().trim());
            pst.setString(3, txtEstatus.getText().trim());

            pst.executeUpdate();

            txtCodigoCurso.setText("");
            txtNombreCurso.setText("");
            txtEstatus.setText("");

            label_status.setText("Registro exitoso.");
        }catch (Exception e){

        }
    }//GEN-LAST:event_btnGuardarActionPerformed

